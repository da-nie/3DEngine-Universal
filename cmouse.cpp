#include "cmouse.h"

CMouse cMouse;

//----------------------------------------------------------------------------------------------------
//конструктор
//----------------------------------------------------------------------------------------------------
CMouse::CMouse()
{
 dX=0;
 dY=0;
 LeftKeyState=false;
 RightKeyState=false;
 CenterKeyState=false;
}
//----------------------------------------------------------------------------------------------------
//деструктор
//----------------------------------------------------------------------------------------------------
CMouse::~CMouse()
{
}
//----------------------------------------------------------------------------------------------------
//получить состояние левой клавиши
//----------------------------------------------------------------------------------------------------
bool CMouse::GetLeftKeyState(void)
{
 return(LeftKeyState);
}
//----------------------------------------------------------------------------------------------------
//получить состояние правой клавиши
//----------------------------------------------------------------------------------------------------
bool CMouse::GetRightKeyState(void)
{
 return(RightKeyState);
}
//----------------------------------------------------------------------------------------------------
//получить состояние центральной клавиши
//----------------------------------------------------------------------------------------------------
bool CMouse::GetCenterKeyState(void)
{
 return(CenterKeyState);
}
//----------------------------------------------------------------------------------------------------
//задать состояние левой клавиши
//----------------------------------------------------------------------------------------------------
void CMouse::SetLeftKeyState(bool state)
{
 LeftKeyState=state;
}
//----------------------------------------------------------------------------------------------------
//задать состояние правой клавиши
//----------------------------------------------------------------------------------------------------
void CMouse::SetRightKeyState(bool state)
{
 RightKeyState=state;
}
//----------------------------------------------------------------------------------------------------
//задать состояние центральной клавиши
//----------------------------------------------------------------------------------------------------
void CMouse::SetCenterKeyState(bool state)
{
 CenterKeyState=state;
}
//----------------------------------------------------------------------------------------------------
//получить смещение по X
//----------------------------------------------------------------------------------------------------
long CMouse::GetXOffset(void)
{
 return(dX);
}
//----------------------------------------------------------------------------------------------------
//получить смещение по Y
//----------------------------------------------------------------------------------------------------
long CMouse::GetYOffset(void)
{
 return(dY);
}
//----------------------------------------------------------------------------------------------------
//задать смещение по X
//----------------------------------------------------------------------------------------------------
void CMouse::SetXOffset(long dx)
{
 dX=dx;
}
//----------------------------------------------------------------------------------------------------
//задать смещение по Y
//----------------------------------------------------------------------------------------------------
void CMouse::SetYOffset(long dy)
{
 dY=dy;
}
